public class Calculadora {
    double soma(double a, double b){
        double resp;
        
        resp = a + b;
        return(resp);
    }
    
    double sub(double a, double b){
        double resp;
        
        resp = b - a;
        return(resp);
    }
    
    double div(double a, double b){
        double resp;
        
        resp = b / a;
        return(resp);
    }
    
    double mult(double a, double b){
        double resp;
        
        resp = a * b;
        return(resp);
    }
}
